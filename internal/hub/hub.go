// Package hub routes incoming messages from platform listeners to connected
// MCP sessions. The daemon creates a single Hub at startup. Listeners call
// Route() when a new message arrives, and the hub delivers it to the
// appropriate Claude Code session via SSE.
package hub

import (
	"context"
	"fmt"
	"log/slog"
	"net/http"
	"sync"
	"time"

	"github.com/anish749/pigeon/internal/account"
	"github.com/anish749/pigeon/internal/claude"
	"github.com/anish749/pigeon/internal/paths"
	"github.com/anish749/pigeon/internal/read"
	"github.com/anish749/pigeon/internal/search"
	"github.com/anish749/pigeon/internal/store"
	"github.com/anish749/pigeon/internal/store/modelv1"
)

// RouteState describes the outcome of routing a message through the hub.
type RouteState int

const (
	RouteOK        RouteState = iota // message signaled to delivery loop
	RouteNoSession                   // no session file exists for this account
)

// RouteResult is returned by Route to tell the caller what happened.
type RouteResult struct {
	State RouteState
}

// MessageNotifyFunc is called by listeners when a new message has been
// written to disk. The msg payload is passed through so the hub can
// publish a pre-formatted event to the broadcast bus without re-reading
// from disk.
type MessageNotifyFunc func(acct account.Account, conversation string, msg modelv1.MsgLine) RouteResult

// ReactionNotifyFunc is called by listeners when a reaction event arrives.
// The react payload is the exact ReactLine written to disk — the hub
// forwards it verbatim to session delivery and the broadcast bus without
// constructing new timestamps or regenerating fields.
type ReactionNotifyFunc func(acct account.Account, conversation string, react modelv1.ReactLine) RouteResult

// EditNotifyFunc is called by listeners when an edit event arrives. The edit
// payload (including ThreadTS when the target lives in a thread) is what the
// hub formats for session delivery.
type EditNotifyFunc func(acct account.Account, conversation string, edit modelv1.EditLine) RouteResult

// DeleteNotifyFunc is called by listeners when a delete event arrives.
type DeleteNotifyFunc func(acct account.Account, conversation string, del modelv1.DeleteLine) RouteResult

// signalBufferSize is the capacity of each channel's delivery signal buffer.
// If full, new signals are dropped — the goroutine will catch up on its
// next drain since it reads all messages since last_delivered.
const signalBufferSize = 64

// Session represents a connected MCP shim process.
type Session struct {
	// SessionID is the pigeon session ID (UUID) generated by `pigeon claude`.
	SessionID string
	// CWD is the working directory of the Claude Code session.
	CWD string
	// Send delivers a message to this session. Provided by the transport
	// layer (SSE) when the session registers.
	Send func(ctx context.Context, notificationMsg NotificationMsg) error
	// Ready is closed when the SSE event loop starts and the session can
	// receive messages. The hub waits on this before sending the hello.
	Ready chan struct{}
}

// channel is a per-platform+account delivery goroutine.
type channel struct {
	acct      account.Account
	sessionID string
	signal    chan deliverySignal
}

type formattedLines []string

// Backwards-compat alias retained for tests and callers that reference the
// reaction-specific name.
type formattedReactionLines = formattedLines

type deliverySignal struct {
	kind         signalKind
	conversation string         // only set for signalNewMessage / Reaction / Edit / Delete
	eventLines   formattedLines // formatted notification lines for Reaction / Edit / Delete
}

type signalKind int

const (
	signalNewMessage signalKind = iota // a specific conversation has a new message
	signalConnected                    // session just connected — send hello and drain all
	signalReaction                     // a reaction event on a specific conversation
	signalEdit                         // an edit event on a specific conversation
	signalDelete                       // a delete event on a specific conversation
)

// Hub manages active MCP sessions and routes incoming messages to them.
type Hub struct {
	mu        sync.RWMutex
	sessions  map[string]*Session // SessionID → connected session
	channels  map[string]*channel // account slug → delivery channel
	store     store.Store
	dataRoot  paths.DataRoot
	broadcast *Broadcast // in-memory fanout bus for /api/tail
	ctx       context.Context
	cancel    context.CancelFunc
}

// New creates a Hub, loads session files, starts delivery goroutines, and
// watches for new session files. Returns an error if session files cannot
// be read (no sessions configured yet is not an error). Call Stop() to shut down.
func New(ctx context.Context, s store.Store, dataRoot paths.DataRoot) (*Hub, error) {
	sessions, err := claude.ListAllSessions()
	if err != nil {
		return nil, fmt.Errorf("load session files: %w", err)
	}

	ctx, cancel := context.WithCancel(ctx)
	h := &Hub{
		sessions:  make(map[string]*Session),
		channels:  make(map[string]*channel),
		store:     s,
		dataRoot:  dataRoot,
		broadcast: NewBroadcast(),
		ctx:       ctx,
		cancel:    cancel,
	}

	for _, s := range sessions {
		h.startChannel(s)
	}
	if len(sessions) > 0 {
		slog.Info("hub started with session channels", "count", len(sessions))
	}

	// Watch for new/updated session files and start channels as needed.
	go h.watchSessionFiles(ctx)

	return h, nil
}

// watchSessionFiles watches for changes in the sessions directory and
// starts delivery channels for any new session files.
func (h *Hub) watchSessionFiles(ctx context.Context) {
	for sessions := range claude.WatchSessions(ctx) {
		h.reconcileChannels(sessions)
	}
}

// reconcileChannels ensures each session file has a delivery channel with
// the correct session ID. Starts new channels and repoints existing ones.
func (h *Hub) reconcileChannels(sessions []*claude.Session) {
	for _, s := range sessions {
		acct := account.New(s.Platform, s.Account)
		key := acct.String()

		h.mu.RLock()
		ch, exists := h.channels[key]
		h.mu.RUnlock()

		if !exists {
			h.startChannel(s)
			slog.Info("new session file detected, delivery channel started",
				"account", acct, "session_id", s.SessionID)
			continue
		}

		// Session file was rewritten with a new session ID — repoint the channel.
		h.mu.Lock()
		if ch.sessionID != s.SessionID {
			if old, ok := h.sessions[ch.sessionID]; ok {
				if err := old.Send(h.ctx, &TextNotificationMsg{
					Text: "pigeon disconnected — a new session took over for " + acct.Display() + ". Restart Claude Code to reconnect.",
				}); err != nil {
					slog.Error("failed to send disconnect notification",
						"session_id", ch.sessionID, "error", err)
				}
			}
			slog.Info("session file changed, delivery channel repointed",
				"account", acct, "old_session", ch.sessionID, "new_session", s.SessionID)
			ch.sessionID = s.SessionID
		}
		h.mu.Unlock()
	}
}

// Stop shuts down all delivery goroutines.
func (h *Hub) Stop() {
	h.cancel()
}

// Register adds a connected MCP shim session to the hub. Validates the
// session ID exists in a session file and the CWD matches.
func (h *Hub) Register(s *Session) error {
	// Validate against session files on disk.
	sessions, err := claude.ListAllSessions()
	if err != nil {
		return fmt.Errorf("validate session: %w", err)
	}
	var found *claude.Session
	for _, cs := range sessions {
		if cs.SessionID == s.SessionID {
			found = cs
			break
		}
	}
	if found == nil {
		return &RegistrationError{
			SessionID:  s.SessionID,
			Reason:     "no session file found — launch via 'pigeon claude' first",
			StatusCode: http.StatusNotFound,
		}
	}
	if found.CWD != s.CWD {
		return &RegistrationError{
			SessionID:  s.SessionID,
			Reason:     "working directory mismatch: session was created in " + found.CWD + " but current directory is " + s.CWD,
			StatusCode: http.StatusBadRequest,
		}
	}

	h.mu.Lock()
	defer h.mu.Unlock()

	if _, exists := h.sessions[s.SessionID]; exists {
		return &RegistrationError{
			SessionID:  s.SessionID,
			Reason:     "this session is already connected in another window",
			StatusCode: http.StatusConflict,
		}
	}
	h.sessions[s.SessionID] = s
	slog.Info("session registered", "session_id", s.SessionID, "cwd", s.CWD)

	// Signal the delivery goroutine to send hello and drain pending messages.
	acct := account.New(found.Platform, found.Account)
	if ch, exists := h.channels[acct.String()]; exists {
		if ch.sessionID != s.SessionID {
			// Notify the old session that it's being replaced.
			if old, ok := h.sessions[ch.sessionID]; ok {
				if err := old.Send(h.ctx, &TextNotificationMsg{
					Text: "pigeon disconnected — a new session took over for " + acct.Display() + ". Restart Claude Code to reconnect.",
				}); err != nil {
					slog.Error("failed to send disconnect notification",
						"session_id", ch.sessionID, "error", err)
				}
			}
			slog.Info("delivery channel repointed to new session",
				"account", acct, "old_session", ch.sessionID, "new_session", s.SessionID)
			ch.sessionID = s.SessionID
		}
		select {
		case ch.signal <- deliverySignal{kind: signalConnected}:
			slog.Info("signalled hello + pending drain on connect", "session_id", s.SessionID)
		default:
		}
	}

	return nil
}

// Unregister removes a session from the hub.
func (h *Hub) Unregister(sessionID string) {
	h.mu.Lock()
	defer h.mu.Unlock()

	if s, exists := h.sessions[sessionID]; exists {
		delete(h.sessions, sessionID)
		slog.Info("session unregistered", "session_id", sessionID, "cwd", s.CWD)
	}
}

// Route signals that a new message has arrived for the given account and
// conversation. Non-blocking — returns immediately.
//
// The message is also published to the broadcast bus for monitor
// subscribers, independent of session state.
func (h *Hub) Route(acct account.Account, conversation string, msg modelv1.MsgLine) RouteResult {
	// Publish to broadcast first — independent of session existence.
	h.broadcast.Publish(NotifMsg{
		Envelope: Envelope{
			Kind:         EventMessage,
			Account:      acct,
			Conversation: conversation,
		},
		MsgLine: msg,
	})

	key := acct.String()

	h.mu.RLock()
	ch, exists := h.channels[key]
	h.mu.RUnlock()

	if !exists {
		slog.Warn("no session configured, message not routed",
			"account", acct, "conversation", conversation)
		return RouteResult{State: RouteNoSession}
	}

	select {
	case ch.signal <- deliverySignal{kind: signalNewMessage, conversation: conversation}:
	default:
		slog.Error("delivery signal buffer full, message delivery may be delayed",
			"account", acct, "conversation", conversation,
			"buffer_size", signalBufferSize)
	}
	return RouteResult{State: RouteOK}
}

// RouteReaction signals that a reaction (or unreaction) event has arrived for
// the given account and conversation. Non-blocking — returns immediately.
//
// The reaction is also published to the broadcast bus for monitor
// subscribers, independent of session state. The ReactLine is forwarded
// as-is — no timestamp or field reconstruction.
func (h *Hub) RouteReaction(acct account.Account, conversation string, react modelv1.ReactLine) RouteResult {
	lines := h.formatReactionLines(acct, conversation, react)
	kind := EventReaction
	if react.Remove {
		kind = EventUnreact
	}
	h.broadcast.Publish(NotifReact{
		Envelope: Envelope{
			Kind:         kind,
			Account:      acct,
			Conversation: conversation,
		},
		ReactLine: react,
	})

	key := acct.String()

	h.mu.RLock()
	ch, exists := h.channels[key]
	h.mu.RUnlock()

	if !exists {
		slog.Warn("no session configured, reaction not routed",
			"account", acct, "conversation", conversation)
		return RouteResult{State: RouteNoSession}
	}

	select {
	case ch.signal <- deliverySignal{kind: signalReaction, conversation: conversation, eventLines: lines}:
	default:
		slog.Error("delivery signal buffer full, reaction delivery may be delayed",
			"account", acct, "conversation", conversation,
			"buffer_size", signalBufferSize)
	}
	return RouteResult{State: RouteOK}
}

// RouteEdit signals that a message edit event has arrived for the given
// account and conversation. Non-blocking — returns immediately. The edit
// is formatted into notification lines (including thread_ts when set on
// the EditLine) and pushed to the connected session.
//
// Note: not yet published on the broadcast bus (no consumers); add when a
// /api/tail subscriber needs edit events.
func (h *Hub) RouteEdit(acct account.Account, conversation string, edit modelv1.EditLine) RouteResult {
	lines := modelv1.FormatEditNotification(edit, time.Local)
	return h.routeEventLines(acct, conversation, signalEdit, "edit", lines)
}

// RouteDelete signals that a message delete event has arrived. Like RouteEdit
// it is non-blocking and pushes formatted lines to the connected session.
func (h *Hub) RouteDelete(acct account.Account, conversation string, del modelv1.DeleteLine) RouteResult {
	lines := modelv1.FormatDeleteNotification(del, time.Local)
	return h.routeEventLines(acct, conversation, signalDelete, "delete", lines)
}

// routeEventLines is shared plumbing for RouteEdit and RouteDelete. It looks
// up the per-account channel and sends a delivery signal carrying the
// pre-formatted lines.
func (h *Hub) routeEventLines(acct account.Account, conversation string, kind signalKind, label string, lines formattedLines) RouteResult {
	key := acct.String()

	h.mu.RLock()
	ch, exists := h.channels[key]
	h.mu.RUnlock()

	if !exists {
		slog.Warn("no session configured, event not routed",
			"kind", label, "account", acct, "conversation", conversation)
		return RouteResult{State: RouteNoSession}
	}

	select {
	case ch.signal <- deliverySignal{kind: kind, conversation: conversation, eventLines: lines}:
	default:
		slog.Error("delivery signal buffer full, event delivery may be delayed",
			"kind", label, "account", acct, "conversation", conversation,
			"buffer_size", signalBufferSize)
	}
	return RouteResult{State: RouteOK}
}

// Sessions returns the number of active (connected) sessions.
func (h *Hub) Sessions() int {
	h.mu.RLock()
	defer h.mu.RUnlock()
	return len(h.sessions)
}

// ClaudeSessionInfo describes a connected Claude Code session.
type ClaudeSessionInfo struct {
	SessionID string
	CWD       string
	Account   string
}

// ConnectedClaudeSessions returns info about all currently connected Claude Code sessions.
func (h *Hub) ConnectedClaudeSessions() []ClaudeSessionInfo {
	h.mu.RLock()
	defer h.mu.RUnlock()

	// Build sessionID → account from channels.
	sessionAcct := make(map[string]string, len(h.channels))
	for _, ch := range h.channels {
		sessionAcct[ch.sessionID] = ch.acct.Display()
	}

	var out []ClaudeSessionInfo
	for id, s := range h.sessions {
		out = append(out, ClaudeSessionInfo{
			SessionID: id,
			CWD:       s.CWD,
			Account:   sessionAcct[id],
		})
	}
	return out
}

// SessionConnected reports whether the given session ID has an active SSE connection.
func (h *Hub) SessionConnected(sessionID string) bool {
	h.mu.RLock()
	defer h.mu.RUnlock()
	_, ok := h.sessions[sessionID]
	return ok
}

// NotifySession sends a text message to a specific session by ID, using the
// same path as incoming messages. Returns an error if the session is not connected.
func (h *Hub) NotifySession(sessionID string, text string) error {
	h.mu.RLock()
	s, ok := h.sessions[sessionID]
	h.mu.RUnlock()

	if !ok {
		return fmt.Errorf("session %s not connected", sessionID)
	}
	return s.Send(h.ctx, &TextNotificationMsg{Text: text})
}

func (h *Hub) startChannel(s *claude.Session) {
	acct := account.New(s.Platform, s.Account)

	ch := &channel{
		acct:      acct,
		sessionID: s.SessionID,
		signal:    make(chan deliverySignal, signalBufferSize),
	}

	h.mu.Lock()
	h.channels[acct.String()] = ch
	h.mu.Unlock()

	go h.deliveryLoop(ch, s.LastDelivered)

	slog.Info("delivery channel started",
		"account", acct, "session_id", s.SessionID)
}

// deliveryLoop waits for signals, reads new messages from disk, and delivers
// them via the connected session. Serialized per channel.
func (h *Hub) deliveryLoop(ch *channel, lastDelivered time.Time) {
	for {
		select {
		case <-h.ctx.Done():
			return
		case sig := <-ch.signal:
			switch sig.kind {
			case signalConnected:
				h.sendHello(ch)
				lastDelivered = h.drainAllConversations(ch, lastDelivered)
			case signalNewMessage:
				lastDelivered = h.drainConversation(ch, sig.conversation, lastDelivered)
			case signalReaction:
				h.deliverEventLines(ch, sig.conversation, sig.eventLines, "reaction")
			case signalEdit:
				h.deliverEventLines(ch, sig.conversation, sig.eventLines, "edit")
			case signalDelete:
				h.deliverEventLines(ch, sig.conversation, sig.eventLines, "delete")
			}
		}
	}
}

// sendHello waits for the session's SSE connection to be ready, then sends a hello.
func (h *Hub) sendHello(ch *channel) {
	h.mu.RLock()
	session := h.sessions[ch.sessionID]
	h.mu.RUnlock()

	if session == nil {
		slog.Warn("sendHello: session not found", "session_id", ch.sessionID)
		return
	}

	// Wait for the SSE event loop to start.
	select {
	case <-session.Ready:
	case <-h.ctx.Done():
		return
	}

	hello := &IncomingMsg{
		Platform:     ch.acct.Platform,
		Account:      ch.acct.Name,
		Conversation: "system",
		MsgLines:     []string{"pigeon connected to " + ch.acct.Display()},
	}
	if err := session.Send(h.ctx, hello); err != nil {
		slog.Error("failed to send hello", "session_id", ch.sessionID, "error", err)
	} else {
		slog.Info("hello sent", "session_id", ch.sessionID, "account", ch.acct)
	}
}

func (h *Hub) drainAllConversations(ch *channel, lastDelivered time.Time) time.Time {
	convs, err := h.store.ListConversations(ch.acct)
	if err != nil {
		slog.Error("failed to list conversations for drain",
			"account", ch.acct, "error", err)
		return lastDelivered
	}
	for _, c := range convs {
		lastDelivered = h.drainConversation(ch, c, lastDelivered)
	}
	return lastDelivered
}

func (h *Hub) drainConversation(ch *channel, conversation string, lastDelivered time.Time) time.Time {
	now := time.Now()
	since := now.Sub(lastDelivered)
	df, readErr := h.store.ReadConversation(ch.acct, conversation, store.ReadOpts{Since: since})
	if df == nil || len(df.Messages) == 0 {
		if readErr != nil {
			slog.Error("failed to read messages",
				"account", ch.acct, "conversation", conversation, "error", readErr)
		}
		return lastDelivered
	}

	convMeta, metaErr := h.store.ReadMeta(ch.acct, conversation)
	lines := modelv1.FormatDateFileNotification(df, time.Local, convMeta, readErr, metaErr)

	// Find connected session.
	h.mu.RLock()
	session := h.sessions[ch.sessionID]
	h.mu.RUnlock()

	if session == nil {
		slog.Warn("session not connected, messages pending",
			"session_id", ch.sessionID, "pending", len(lines))
		return lastDelivered
	}

	msg := &IncomingMsg{
		Platform:     ch.acct.Platform,
		Account:      ch.acct.Name,
		Conversation: conversation,
		MsgLines:     lines,
	}
	if err := session.Send(h.ctx, msg); err != nil {
		slog.Error("failed to deliver message",
			"session_id", ch.sessionID, "error", err)
		return lastDelivered
	}

	// Update cursor to now — everything up to this point has been delivered.
	if err := claude.UpdateLastDelivered(ch.acct, now); err != nil {
		slog.Error("failed to update last_delivered",
			"session_id", ch.sessionID, "error", err)
	}
	return now
}

// deliverEventLines sends already-formatted event lines (reaction, edit,
// delete) to the connected session. These events are delivered out-of-band:
// not gated by the last_delivered cursor, since the target may be older
// than the cursor.
func (h *Hub) deliverEventLines(ch *channel, conversation string, lines formattedLines, kind string) {
	h.mu.RLock()
	session := h.sessions[ch.sessionID]
	h.mu.RUnlock()

	if session == nil {
		slog.Warn("session not connected, event dropped",
			"kind", kind, "session_id", ch.sessionID, "account", ch.acct, "conversation", conversation)
		return
	}

	notification := &IncomingMsg{
		Platform:     ch.acct.Platform,
		Account:      ch.acct.Name,
		Conversation: conversation,
		MsgLines:     lines,
	}
	if err := session.Send(h.ctx, notification); err != nil {
		slog.Error("failed to deliver event",
			"kind", kind, "session_id", ch.sessionID, "account", ch.acct, "error", err)
	}
}

func (h *Hub) formatReactionLines(acct account.Account, conversation string, react modelv1.ReactLine) formattedReactionLines {
	msg, threadTS := h.lookupMessage(acct, conversation, react.MsgID)
	if msg != nil {
		return modelv1.FormatReactionNotification(*msg, react, threadTS, time.Local)
	}
	return modelv1.FormatReactionFallbackNotification(react, threadTS, time.Local)
}

// lookupMessage searches for a message by ID in a conversation. Returns the
// message and, when the message lives in a thread file, the thread's TS.
// Returns (nil, "") if not found.
//
// Uses rg's --json output via read.Grep + search.ParseGrepOutput so the
// caller consumes structured matches (already-parsed modelv1.Line values
// and thread-vs-date file classification) instead of re-parsing rg's text
// output here.
func (h *Hub) lookupMessage(acct account.Account, conversation, msgID string) (*modelv1.MsgLine, string) {
	convDir := h.dataRoot.AccountFor(acct).Conversation(conversation).Path()
	out, err := read.Grep(convDir, read.GrepOpts{
		Query:        msgID,
		FixedStrings: true,
		JSON:         true,
	})
	if err != nil || len(out) == 0 {
		return nil, ""
	}

	// Parse paths relative to the account dir so search.ParseFilePath can
	// recover the conversation segment; the grep itself is already scoped
	// to convDir so cross-conversation matches aren't possible.
	acctDir := h.dataRoot.AccountFor(acct).Path()
	matches, _ := search.ParseGrepOutput(out, acctDir)
	for _, m := range matches {
		if m.Line.Type != modelv1.LineMessage || m.Line.Msg == nil || m.Line.Msg.ID != msgID {
			continue
		}
		threadTS := ""
		if m.Thread {
			threadTS = m.Date
		}
		return m.Line.Msg, threadTS
	}
	return nil, ""
}

// RegistrationError is returned when session registration fails.
type RegistrationError struct {
	SessionID  string
	Reason     string
	StatusCode int // HTTP status code to return to the client.
}

func (e *RegistrationError) Error() string {
	return "session " + e.SessionID + ": " + e.Reason
}
